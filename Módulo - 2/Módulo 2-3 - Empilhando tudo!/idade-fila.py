# ORDEM IDADE

# Criando uma lista
queue = ["21 anos","23 anos","25 anos"]

# Mostra a lista criada
print(queue)

# Adicionado na última posição da lista 
queue.append("30 anos")

# Mostra 30 anos adicionado na última posição 
print(queue)

# O primeiro será tirado da Fila |  First In First Out, assim a posição 1 torna-se posição 0
queue.pop(0)

# Mostra a lista sem o primeiro elento da fila "21 anos"
print(queue)