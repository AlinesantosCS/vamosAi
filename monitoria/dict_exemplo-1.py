d = {
    "a": 1,
    "b": 2,
    "c": 3
}

# keys
# ["a", "b", "c"]
for key in d.keys():
    print(key)
    print(d[key])

# values
# [1, 2, 3]
for value in d.values():
    print(value)

# items
# [("a", 1), ("b", 2), ("c", 3)]
for key, value in d.items():
    print(key, value)
    
